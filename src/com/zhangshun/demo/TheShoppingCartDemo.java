package com.zhangshun.demo;

import android.widget.CheckBox;

public class TheShoppingCartDemo {

	int img;
	String the_shopping_title,
	the_shopping_content,
	the_shopping_number,
	The_price;
	CheckBox Future_generations;

	public CheckBox getFuture_generations() {
		return Future_generations;
	}

	public void setFuture_generations(CheckBox future_generations) {
		Future_generations = future_generations;
	}

	public String getThe_shopping_title() {
		return the_shopping_title;
	}

	public void setThe_shopping_title(String the_shopping_title) {
		this.the_shopping_title = the_shopping_title;
	}

	public String getThe_shopping_content() {
		return the_shopping_content;
	}

	public void setThe_shopping_content(String the_shopping_content) {
		this.the_shopping_content = the_shopping_content;
	}

	public String getThe_shopping_number() {
		return the_shopping_number;
	}

	public void setThe_shopping_number(String the_shopping_number) {
		this.the_shopping_number = the_shopping_number;
	}

	public String getThe_price() {
		return The_price;
	}

	public void setThe_price(String the_price) {
		The_price = the_price;
	}

	public int getImg() {
		return img;
	}

	public void setImg(int img) {
		this.img = img;
	}

}
