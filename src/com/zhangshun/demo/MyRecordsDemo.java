package com.zhangshun.demo;

public class MyRecordsDemo {
	private int image;
	private String my_records_title;
	private String my_records_content;
	private int my_records_intent;

	public int getImage() {
		return image;
	}

	public void setImage(int image) {
		this.image = image;
	}

	public String getMy_records_title() {
		return my_records_title;
	}

	public void setMy_records_title(String my_records_title) {
		this.my_records_title = my_records_title;
	}

	public String getMy_records_content() {
		return my_records_content;
	}

	public void setMy_records_content(String my_records_content) {
		this.my_records_content = my_records_content;
	}

	public int getMy_records_intent() {
		return my_records_intent;
	}

	public void setMy_records_intent(int my_records_intent) {
		this.my_records_intent = my_records_intent;
	}

}
